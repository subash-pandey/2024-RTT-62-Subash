package org.example_Loops_conditions;

public class PracticeWhileLoop {
    public static void main(String[] args) {
       int x =1;
       while(x<=10){
           System.out.println(x);
           x=x+1;
       }

       do{

           System.out.println(x);
           x=x+1;
       }while(x<=20);
    }
}
