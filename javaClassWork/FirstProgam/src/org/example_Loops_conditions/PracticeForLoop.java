package org.example_Loops_conditions;

public class PracticeForLoop {
    public static void main(String[] args) {
        for (int i = 91; i <= 99; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.println(i + " * " + j + " = " + i * j);
            }

        }
    }
}
